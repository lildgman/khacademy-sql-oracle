SELECT 년도, 학기 AS 학기, 학점
FROM (
    SELECT SUBSTR(TERM_NO,1,4) AS 년도, SUBSTR(TERM_NO,5,2) AS 학기, POINT AS 학점
    FROM TB_GRADE
    WHERE STUDENT_NO = 'A112113'
    UNION
    SELECT 년도, NULL AS 년도, ROUND(AVG(학점),1) AS 평점
    FROM (SELECT SUBSTR(TERM_NO,1,4) AS 년도, SUBSTR(TERM_NO,5,2) AS 학기, POINT AS 학점
            FROM TB_GRADE
            WHERE STUDENT_NO = 'A112113')
GROUP BY 년도
ORDER BY 년도, 학기 ASC
)
UNION 
SELECT NULL, NULL,ROUND(AVG(학점),1)
FROM (SELECT SUBSTR(TERM_NO,1,4) AS 년도, SUBSTR(TERM_NO,5,2) AS 학기, POINT AS 학점
    FROM TB_GRADE
    WHERE STUDENT_NO = 'A112113');

SELECT SUBSTR(TERM_NO, 1, 4) 년도, SUBSTR(TERM_NO, 5, 2) 학기, ROUND(AVG(POINT), 1) 평균
FROM TB_GRADE
WHERE STUDENT_NO = 'A112113'
GROUP BY ROLLUP(SUBSTR(TERM_NO, 1, 4), SUBSTR(TERM_NO, 5, 2))
ORDER BY 1; 
