SELECT STUDENT_NO AS 학번,
    STUDENT_NAME AS 이름,
    TO_CHAR(ENTRANCE_DATE,'YYYY-MM-DD') AS 입학년도
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '영어영문학과'
ORDER BY ENTRANCE_DATE;

-- 2.
SELECT PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE PROFESSOR_NAME NOT LIKE '___';

-- 3.
SELECT PROFESSOR_NAME AS 교수이름,
    CASE
        WHEN TO_CHAR(SYSDATE,'MMDD') < TO_CHAR(TO_DATE(SUBSTR(PROFESSOR_SSN,1,6)),'MMDD')
        THEN EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM TO_DATE(SUBSTR(PROFESSOR_SSN,1,6))) - 1
        ELSE EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM TO_DATE(SUBSTR(PROFESSOR_SSN,1,6)))
        END AS 만나이
FROM TB_PROFESSOR
WHERE SUBSTR(PROFESSOR_SSN,8,1) IN (1,3);

