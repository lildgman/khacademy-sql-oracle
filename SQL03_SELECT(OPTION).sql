--9.
SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS
JOIN TB_CLASS_PROFESSOR USING (CLASS_NO)
JOIN TB_PROFESSOR P USING (PROFESSOR_NO)
JOIN TB_DEPARTMENT D ON (P.DEPARTMENT_NO = D.DEPARTMENT_NO)
WHERE D.CATEGORY = '인문사회'
ORDER BY PROFESSOR_NAME;

-- 10.
SELECT 학번, "학생 이름", ROUND(AVG(평점),1)
FROM (
    SELECT STUDENT_NO AS 학번, STUDENT_NAME AS "학생 이름", POINT AS 평점
    FROM TB_STUDENT
    JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
    JOIN TB_GRADE USING (STUDENT_NO)
    WHERE DEPARTMENT_NAME = '음악학과'
    )
GROUP BY 학번, "학생 이름"
ORDER BY 학번;

SELECT G.STUDENT_NO AS 학번, STUDENT_NAME AS "학생 이름", ROUND(AVG(POINT),1) AS 평점
FROM TB_GRADE G
    JOIN TB_STUDENT S ON (G.STUDENT_NO = S.STUDENT_NO)
    JOIN TB_DEPARTMENT D ON (S.DEPARTMENT_NO = D.DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '음악학과'
GROUP BY G.STUDENT_NO, STUDENT_NAME;

-- 11.
SELECT DEPARTMENT_NAME AS 학과이름, STUDENT_NAME AS 학생이름, PROFESSOR_NAME AS 지도교수이름
FROM TB_STUDENT
    JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
    JOIN TB_PROFESSOR ON (COACH_PROFESSOR_NO = PROFESSOR_NO)
WHERE STUDENT_NO = 'A313047';

-- 12.
SELECT STUDENT_NAME, TERM_NO
FROM TB_STUDENT
    JOIN TB_GRADE USING (STUDENT_NO)
    JOIN TB_CLASS ON (TB_GRADE.CLASS_NO = TB_CLASS.CLASS_NO)
WHERE SUBSTR(TERM_NO,1,4) = '2007'
    AND CLASS_NAME = '인간관계론';

-- 13.
SELECT CLASS_NAME, DEPARTMENT_NAME
FROM TB_CLASS
    LEFT JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
    LEFT JOIN TB_CLASS_PROFESSOR USING (CLASS_NO)
WHERE CATEGORY = '예체능'
    AND PROFESSOR_NO IS NULL
ORDER BY DEPARTMENT_NAME;

-- 14.
SELECT STUDENT_NAME AS 학생이름, NVL(PROFESSOR_NAME,'지도교수 미지정') AS 지도교수
FROM TB_STUDENT
    JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
    LEFT JOIN TB_PROFESSOR ON (COACH_PROFESSOR_NO = PROFESSOR_NO)
WHERE DEPARTMENT_NAME = '서반아어학과';

-- 15.
SELECT STUDENT_NO AS 학번, STUDENT_NAME AS 학생이름, DEPARTMENT_NAME AS 학과이름, ROUND(AVG(POINT),1) AS 학점
FROM TB_STUDENT
    JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
    JOIN TB_GRADE USING (STUDENT_NO)
WHERE ABSENCE_YN = 'N'
GROUP BY STUDENT_NO, STUDENT_NAME, DEPARTMENT_NAME
HAVING AVG(POINT) >= 4.0
ORDER BY 학번;

-- 16.
SELECT CLASS_NO, CLASS_NAME, ROUND(AVG(POINT),8)
FROM TB_CLASS
    JOIN TB_GRADE USING (CLASS_NO)
    JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '환경조경학과' AND CLASS_TYPE LIKE '전공%'
GROUP BY CLASS_NO, CLASS_NAME
ORDER BY CLASS_NO;

-- 17.
SELECT STUDENT_NAME, STUDENT_ADDRESS
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = (
            SELECT DEPARTMENT_NAME
            FROM TB_STUDENT
            JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
            WHERE STUDENT_NAME = '최경희'
        );

-- 18.
SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
    JOIN TB_GRADE USING (STUDENT_NO)
    JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '국어국문학과'
GROUP BY STUDENT_NO, STUDENT_NAME
HAVING AVG(POINT) = (
    SELECT MAX(평점)
    FROM (SELECT STUDENT_NO, STUDENT_NAME,AVG(POINT) AS 평점
    FROM TB_STUDENT
        JOIN TB_GRADE USING (STUDENT_NO)
        JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
    WHERE DEPARTMENT_NAME = '국어국문학과'
    GROUP BY STUDENT_NO, STUDENT_NAME));
    
-- 19.
SELECT DEPARTMENT_NAME AS "계열 학과명", ROUND(AVG(POINT),1) AS 전공평점
FROM TB_DEPARTMENT
    JOIN TB_CLASS USING (DEPARTMENT_NO)
    JOIN TB_GRADE USING (CLASS_NO)
WHERE CATEGORY = (
    SELECT CATEGORY
    FROM TB_DEPARTMENT
    WHERE DEPARTMENT_NAME = '환경조경학과'
)
GROUP BY DEPARTMENT_NAME
ORDER BY "계열 학과명";